//loops  can excute a block of  code as long as specified  condition is reached
// are handy  because they save time , reduce errors and  they  make cod more readable

// while loop
// while (condition) {
//   // code block to be executed
// }

// he loop will always be executed at least once, even if the condition is false, because the code block is executed before the condition is tested
public class whileloop{
    public static void main(String[] args){
        int  i  =  0;
        while (i < 20){
            System.out.println(i);
            i++;

        }
    }
}